#include "3-calc.h"
/**
 * op_add - function prototype
 * Description: returns the sum of a and b
 * @a: input int
 * @b: input int
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - function prototype
 * Description: returns the difference of a and b
 * @a: input int
 * @b: input int
 * Return: difference of a and b
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - function prototype
 * Description: returns the product of a and b
 * @a: input int
 * @b: input int
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - function prototype
 * Description: returns the result of the division of a by b
 * @a: input int
 * @b: input int
 * Return: quotient of a and b
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - function prototype
 * Description: returns the remainder of the division of a by b
 * @a: the first integer
 * @b: the second integer
 * Return: modulus of a by b
 */
int op_mod(int a, int b)
{
return (a % b);
}
